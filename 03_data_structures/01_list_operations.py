# BUILDING A LIST MANAGER
my_list = [10, 25, 3, 47, 8]
my_list.append(15) # add 15 to the list
my_list.insert(2, 99) # insert 99 at index 2
my_list.remove(47) # remove the first occurrence of 47 from the list
my_list.pop(3) # remove the element at index 3 and return it
my_list.sort() # sort the list in ascending order
my_list.reverse() # reverse the order of the list

print("Final List:", my_list)


